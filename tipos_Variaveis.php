<?php

//Tipos de variaveis

//Tipo STRING
$minhaVar = "Texto";
echo($minhaVar . '<br />'); // resultado: TEXTO

//Tipo BOOLEAN
$minhaVar = true;
echo($minhaVar . '<br />'); // resultado: 1
//Tipo BOOLEAN
$minhaVar = false;
echo($minhaVar . '<br />'); // resultado:  <Vazio>


//Tipo FLOAT
$minhaVar = 5.22;
echo($minhaVar . '<br />'); // resultado: 5.22
?>