<?php

//Tipos de variaveis

//Tipo STRING
$minhaVar = "Texto";
echo($minhaVar . "<br />"); // resultado: TEXTO

//Tipo BOOLEAN
$minhaVar = true;
echo($minhaVar . '<br />'); // resultado: 1

//Tipo BOOLEAN
$minhaVar = false;
echo($minhaVar . '<br />'); // resultado:  <Vazio>

//Tipo DOBLE
$minhaVar = 5.35;
echo($minhaVar . '<br />'); // resultado: 5.22

//Tipo INTEGER
$minhaVar = 5;
echo($minhaVar . '<br />'); // resultado: 5.22

//Tipo INTEGER
$minhaVar = 10;
echo($minhaVar . '<br />'); // resultado: 5.22
?>